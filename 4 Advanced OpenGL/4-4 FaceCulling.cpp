/*
	Face culling
	实际上，对于一个立方体，人最多只能看到其3个面。
	所以如果我们只渲染我们能看到的部分，那么性能将会提高50%甚至更多！

	不过这里的一个问题是：
		我们怎么知道我们看到的是哪个面呢？
			OpenGL允许检查所有正面朝向（Front facing）观察者的面，并渲染他们
			然后丢弃所有背向观察者的面，
		
		我们所要做的就是：
			告诉OpenGL那些面朝向我们，以及哪些面背向

	正面与背面的判断：
	Winding Order
		OpenGL通过顶点顺序来分析这样的问题

			当我们定义一组顶点时，我们以一种特定的顶点顺序定义，如顺时针或者逆时针。

			float vertices[] = {
			// clockwise
			vertices[0], // vertex 1
			vertices[1], // vertex 2
			vertices[2], // vertex 3
			// counter-clockwise
			vertices[0], // vertex 1
			vertices[2], // vertex 3
			vertices[1]  // vertex 2
			};

			这里每三个顶点构成一个基本三角形，
			当渲染的时候，OpenGL默认认为：
					逆时针顶点定义的三角形为三角形的正面。

				即：当定义顶点顺序为逆时针，则你所看到的三角形是朝向你的。

			光栅化阶段；指定所有的顶点，为逆时针向，顶点连接顺序在光栅化阶段计算。
			所以当顶点着色器已经运行后。顶点就能够在观察者的观察点被看到。
*/

